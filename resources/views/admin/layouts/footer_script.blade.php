                var formAction = $(this).attr('action') || '';
                var formMethod = ($(this).attr('method') || 'GET').toUpperCase();

                // Allow GET forms, logout forms, and search forms
                if (formMethod === 'GET') return true;
                if (formAction.indexOf('logout') !== -1) return true;

                e.preventDefault();
                e.stopImmediatePropagation();
                showDemoToast();
                return false;
            });

            // Intercept button clicks (submit, edit, update, etc.)

    // Demo Mode: Intercept all action clicks on /admin URLs
    $(document).ready(function() {
        var currentPath = window.location.pathname;
        if (currentPath.indexOf('/admin') === 0 || currentPath.indexOf('/admin') !== -1) {
            var demoMessage = 'You do not have permission to perform this action in demo mode.';
            var demoToastActive = false;

            function showDemoToast() {
                // Prevent duplicate toasts firing within a short window
                if (demoToastActive) return;
                demoToastActive = true;
                setTimeout(function() { demoToastActive = false; }, 1000);

                iziToast.error({
                    title: demoMessage,
                    position: 'topRight',
                    timeout: 5000,
                    icon: 'fas fa-lock',
                    transitionIn: 'bounceInLeft',
                    transitionOut: 'fadeOutRight',

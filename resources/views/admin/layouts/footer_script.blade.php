                // Allow logout buttons
                if ($(this).closest('form').find('a[href*="logout"]').length > 0) return true;
                if ($(this).closest('#logout-form').length > 0) return true;

                e.preventDefault();
                e.stopImmediatePropagation();
                showDemoToast();
                return false;
            });

            // Intercept AJAX 403 responses (fallback for any AJAX calls that bypass click interception)
            $(document).ajaxError(function(event, jqXHR, settings, thrownError) {
                if (jqXHR.status === 403 && jqXHR.responseJSON && jqXHR.responseJSON.error) {

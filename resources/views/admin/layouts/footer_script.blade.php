                    messageColor: '#fff',
                    iconColor: '#fff',
                    progressBarColor: '#fff',
                });
            }

            // Override SweetAlert popup functions to show iziToast instead
            window.showDeletePopupModal = function(url, options) { showDemoToast(); };
            window.showRestorePopupModal = function(url, options) { showDemoToast(); };
            window.showPermanentlyDeletePopupModal = function(url, options) { showDemoToast(); };
            window.showSweetAlertConfirmPopup = function(url, method, options) { showDemoToast(); };

            // Intercept form submissions
